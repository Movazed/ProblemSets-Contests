class SecretArrayHandler {
private:
    vector<int> secretArray;
    int mysteriousCounter;

    void fillSecretArray() {
        for (int i = 2; i < MAX_SIZE; ++i) {
            if (secretArray[i] == -1) {
                mysteriousCounter++;
                if (i == 2) mysteriousCounter = 0;
                if (i == 3) mysteriousCounter = 2;
                for (int j = i; j < MAX_SIZE; j += i) {
                    if (secretArray[j] == -1) {
                        secretArray[j] = mysteriousCounter;
                    }
                }
            }
            if (secretArray[i] == -1) {
                mysteriousCounter++;
            }
        }
    }

public:
    SecretArrayHandler() : secretArray(MAX_SIZE, -1), mysteriousCounter(0) {
        secretArray[0] = 0;
        secretArray[1] = 1;
        fillSecretArray();
    }

    int getValue(int index) const {
        return secretArray[index];
    }
};

